package multiTherading.executor;

public class FileLoaderException extends Exception {

	public FileLoaderException() {
	}

	public FileLoaderException(String arg0) {
		super(arg0);
	}

	public FileLoaderException(Throwable arg0) {
		super(arg0);
	}

	public FileLoaderException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public FileLoaderException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
